cursor.execute("DROP TABLE IF EXISTS magazin")

cursor.execute("""CREATE TABLE users (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    age INTEGER NOT NULL
    )""")

cursor.execute("""CREATE TABLE magazin (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    nazvaniye TEXT NOT NULL,
    proizvoditel TEXT NOT NULL,
    country TEXT NOT NULL,
    goden TEXT NOT NULL
    )""")

cursor.execute("""CREATE TABLE borrowings (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    user_id INTEGER NOT NULL,
    product_id INTEGER NOT NULL,
    date_borrowed TEXT NOT NULL,
    date_returned TEXT,
    FOREIGN KEY (user_id) REFERENCES users(id),
    FOREIGN KEY (product_id) REFERENCES magazin(id)
    )""")

cursor.execute("""CREATE TABLE delivery (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    user_id INTEGER NOT NULL,
    product_id INTEGER NOT NULL,
    address TEXT NOT NULL,
    date TEXT NOT NULL,
    status TEXT NOT NULL,
    FOREIGN KEY (user_id) REFERENCES users(id),
    FOREIGN KEY (product_id) REFERENCES magazin(id)
    )""")

